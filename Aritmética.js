// Math.pow(2,53) => 9007199254740992: 2 elevado à potência 53
console.log("2 elevado à potência 53: " + Math.pow(2, 53));

// Math.round(.6) => 1.0: arredonda para o inteiro mais próximo
console.log("Arredondado: " + Math.round(0.6));

// Math.ceil(.6) => 1.0: arredonda para cima para um inteiro
console.log("Arredondado para cima: " + Math.ceil(0.6));

// Math.floor(.6) => 0.0: arredonda para baixo para um inteiro
console.log("Arredondado para baixo: " + Math.floor(0.6));

// Math.abs(-5) => 5: valor absoluto
console.log("Valor absoluto de -5: " + Math.abs(-5));

// Math.max(x,y,z) => Retorna o maior argumento
console.log("Maior valor entre 10, 5 e 7: " + Math.max(10, 5, 7));

// Math.min(x,y,z) => Retorna o menor argumento
console.log("Menor valor entre 10, 5 e 7: " + Math.min(10, 5, 7));

// Math.random() => Número pseudoaleatório x, onde 0 <= x < 1.0
console.log("Número pseudoaleatório: " + Math.random());

// Math.PI => π: circunferência de um círculo / diâmetro
console.log("Valor de π (pi): " + Math.PI);

// Math.E => e: A base do logaritmo natural
console.log("Valor de e (base do logaritmo natural): " + Math.E);

// Math.sqrt(3) => A raiz quadrada de 3
console.log("Raiz quadrada de 3: " + Math.sqrt(3));

// Math.pow(3, 1/3) => A raiz cúbica de 3
console.log("Raiz cúbica de 3: " + Math.pow(3, 1 / 3));

// Math.sin(0) => Trigonometria: também Math.cos, Math.atan, etc.
console.log("Seno de 0: " + Math.sin(0));

// Math.log(10) => Logaritmo natural de 10
console.log("Logaritmo natural de 10: " + Math.log(10));

// Math.log(100)/Math.LN10 => Logaritmo de base 10 de 100
console.log("Logaritmo de base 10 de 100: " + Math.log(100) / Math.LN10);

// Math.log(512)/Math.LN2 => Logaritmo de base 2 de 512
console.log("Logaritmo de base 2 de 512: " + Math.log(512) / Math.LN2);

// Math.exp(3) => Math.E ao cubo
console.log("e (Math.E) elevado ao cubo: " + Math.exp(3));
